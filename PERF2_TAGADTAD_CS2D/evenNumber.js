// Write a program that prints numbers from 1 to 20 using a for loop
for (let i = 1; i <= 20; i++) {
    console.log(i);
}

// Modify the program to print only even numbers between 1 and 20
console.log("Even numbers between 1 and 20:");
for (let i = 1; i <= 20; i++) {
    if (i % 2 === 0) {
        console.log(i);
    }
}
